package com.psj.welfare.Data;

public class BenefitDTO
{
    private String benefit_no;
    private String welf_name;
    private String welf_target;
    private String welf_contents;
    private String welf_period;
    private String welf_apply;
    private String welf_contact;

    public String getBenefit_no()
    {
        return benefit_no;
    }

    public void setBenefit_no(String benefit_no)
    {
        this.benefit_no = benefit_no;
    }

    public String getWelf_name()
    {
        return welf_name;
    }

    public void setWelf_name(String welf_name)
    {
        this.welf_name = welf_name;
    }

    public String getWelf_target()
    {
        return welf_target;
    }

    public void setWelf_target(String welf_target)
    {
        this.welf_target = welf_target;
    }

    public String getWelf_contents()
    {
        return welf_contents;
    }

    public void setWelf_contents(String welf_contents)
    {
        this.welf_contents = welf_contents;
    }

    public String getWelf_period()
    {
        return welf_period;
    }

    public void setWelf_period(String welf_period)
    {
        this.welf_period = welf_period;
    }

    public String getWelf_apply()
    {
        return welf_apply;
    }

    public void setWelf_apply(String welf_apply)
    {
        this.welf_apply = welf_apply;
    }

    public String getWelf_contact()
    {
        return welf_contact;
    }

    public void setWelf_contact(String welf_contact)
    {
        this.welf_contact = welf_contact;
    }
}
